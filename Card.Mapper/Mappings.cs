using Card.Model;
using Card.Utility.ViewModel;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Card.Mapper
{
    public static class Mappings
    {
        public static CardNumberViewModel ConvertToView(this CardNumber cardNumber)
        {
            CardNumberViewModel cardNumberViewModel = new CardNumberViewModel
            {
                id = cardNumber.Id,
                cNumber = cardNumber.CNumber,
                active = cardNumber.Active,
                author = cardNumber.Author,
                editor = cardNumber.Editor,
                created = cardNumber.Created,
                modified = cardNumber.Modified
            };
            return cardNumberViewModel;
        }
        public static CardTypeViewModel ConvertToView(this CardType cardType)
        {
            CardTypeViewModel cardTypeViewModel = new CardTypeViewModel
            {
                id = Convert.ToInt16(cardType.Id),
                name = cardType.Name,
                prefix= cardType.Prefix
            };
            return cardTypeViewModel;
        }
        public static List<CardTypeViewModel> ConvertToViews(this List<CardType> cardTypes)
        {
            List<CardTypeViewModel> cardTypeViews = new List<CardTypeViewModel>();

            foreach (CardType x in cardTypes)
            {
                cardTypeViews.Add(x.ConvertToView());
            }
            return cardTypeViews;
        }
    }
}
